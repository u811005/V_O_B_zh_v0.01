export function findLineByKeys(group, keys) {
  if (!group) return null;
  const key = keys.find(k => group[k]);
  return key ? group[key] : null;
}

export function expandEventVillagerLines(group) {
  if (!group) return null;

  const expanded = { ...group };
  Object.entries(SPEECH_TYPE_LINE_FALLBACKS).forEach(([speechType, fallbackKeys]) => {
    if (!expanded[speechType]) {
      expanded[speechType] = findLineByKeys(group, fallbackKeys);
    }
  });

  return expanded;
}

export const EVENT_LINES_BY_SPEECH_TYPE = Object.fromEntries(
  Object.entries(EVENT_SPEECH_TYPE_LINES).map(([eventKey, group]) => [
    eventKey,
    expandEventVillagerLines(group)
  ])
);

